package com.models;

public class Compress {
	private String id;
	 private String value;
	  
	public Compress(String id, String value) {
		super();
		this.id = id;
		this.value = value;
	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}
	public void setValor(String value) {
		this.value = value;
	}
	 
	 
	 
	
}

