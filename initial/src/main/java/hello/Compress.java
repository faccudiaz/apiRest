package hello;

public class Compress {
	private String id;
	 private String valor;
	  
	public Compress(String id, String valor) {
		super();
		this.id = id;
		this.valor = valor;
	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	 
	 
	 
	
}
